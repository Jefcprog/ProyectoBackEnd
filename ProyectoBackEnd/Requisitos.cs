 * Enunciado del Proyecto Final: Ingeniería de Software 2
"Diseño e Implementación de la Interfaz de Usuario (UI) y Experiencia de Usuario (UX) para una Gestión Básica de Usuarios"
Descripción:
El objetivo del proyecto es diseñar y desarrollar una interfaz de usuario (UI) funcional y atractiva para un sistema básico 
de gestión de usuarios con operaciones CRUD (Crear, Leer, Actualizar, Eliminar). Este proyecto busca integrar los principios 
de UI y UX aprendidos en clase y aplicar conocimientos básicos sobre el manejo de bases de datos.
Características del Sistema:
1.	Base de Datos Básica:
o	La base de datos debe contener al menos dos tablas esenciales relacionadas con la gestión de usuarios (ejemplo:
tabla de usuarios y tabla de autenticación).
o	Operaciones a implementar:
	Registro de nuevos usuarios.
	Inicio de sesión por parte del usuario.
	Ingreso, edición y eliminación de datos personales (como nombre, apellido, cédula, etc.).
	Búsqueda de usuarios por apellido o cédula.
	Visualización de la lista de usuarios registrados.
2.	Interfaz de Usuario:
o	Diseñar una interfaz visualmente atractiva, accesible e intuitiva.
o	Implementar elementos básicos como formularios de entrada, tablas de datos, botones y cuadros de búsqueda.
o	Aplicar principios de diseño responsivo y accesibilidad.
3.	Tecnologías:
o	La interfaz debe estar desarrollada usando tecnologías web o de escritorio adecuadas (HTML/CSS, JavaScript, Angular).
o	La base de datos puede ser cualquiera (SQL SERVER.).
4.	Exposición del Proyecto:
o	Presentar una demostración funcional del sistema, donde se pueda interactuar directamente con la interfaz y
realizar operaciones sobre la base de datos.
o	No se aceptarán presentaciones con solo capturas de pantalla; el sistema debe estar operativo.
5.	Entregables:
o	Código fuente del proyecto.
o	Documentación técnica que incluya:
	Esquema de la base de datos.
	Diseño de la UI/UX.
	Capturas del diseño.
